async function postJson(url, body) {
  const response = await fetch(url, {
    method: "POST",
    headers: {
      "content-type": "application/json",
    },
    body: JSON.stringify(body),
  });

  return response;
}

async function readEvent(streamReader) {
  while (true) {
    const frame = await streamReader.nextFrame();
    if (!frame) {
      throw new Error("Expected an SSE frame but the stream ended.");
    }

    if (frame.event === "ping") {
      continue;
    }

    return frame.data;
  }
}

async function readUntilType(streamReader, eventType) {
  while (true) {
    const event = await readEvent(streamReader);
    if (event.type === eventType) {
      return event;
    }
  }
}

async function openSse(url) {
  const controller = new AbortController();
  const response = await fetch(url, {
    headers: {
      accept: "text/event-stream",
    },
    signal: controller.signal,
  });

  assert.equal(response.status, 200);

  const decoder = new TextDecoder();
  const reader = response.body.getReader();
  let buffer = "";

  return {
    async nextFrame() {
      while (true) {
        const splitIndex = buffer.indexOf("\n\n");
        if (splitIndex >= 0) {
          const rawFrame = buffer.slice(0, splitIndex);
          buffer = buffer.slice(splitIndex + 2);
          if (!rawFrame.trim()) {
            continue;
          }

          const frame = {
            event: "message",
            data: undefined,
          };

          for (const line of rawFrame.split("\n")) {
            if (line.startsWith("event:")) {
              frame.event = line.slice(6).trim();
            }
            if (line.startsWith("data:")) {
              frame.data = JSON.parse(line.slice(5).trimStart());
            }
          }

          return frame;
        }

        const { done, value } = await reader.read();
        if (done) {
          return null;
        }
        buffer += decoder.decode(value, { stream: true });
      }
    },
    close() {
      controller.abort();
    },
  };
}

test("streams the same normalized events to multiple SSE clients", async (t) => {
  const { service, workspaceRoot } = await createHarness(t);
  const server = createControlPlaneServer({
    service,
  });
  let browserClient = null;
  let stxClient = null;

  t.after(async () => {
    browserClient?.close();
    stxClient?.close();
    server.close();
    await once(server, "close");
  });

  server.listen(0, "127.0.0.1");
  await once(server, "listening");

  const { port } = server.address();
  const baseUrl = `http://127.0.0.1:${port}`;

  await service.createOrUpdateIntent({
    runId: "run-sse",
    primaryDirective: "Implement event streaming.",
    strictBoundaries: ["Keep the backend authoritative."],
    successState: "Both clients receive normalized updates.",
    actor: "test",
  });

  browserClient = await openSse(`${baseUrl}/runs/run-sse/events`);
  stxClient = await openSse(`${baseUrl}/runs/run-sse/events`);

  const artifact = await compileRun(service, "run-sse", workspaceRoot);

  const pausedArtifact = await service.executeApprovedNodes({
    runId: "run-sse",
    actor: "operator",
  });
  const executableNode = pausedArtifact.plan.nodes.find(
    (node) => node.nodeType === "deterministic_execution",
  );
  const approvalGate = pausedArtifact.plan.approvalGates.find((gate) => gate.required);

  const artifactEventA = await readUntilType(browserClient, "artifact.generated");
  const artifactEventB = await readUntilType(stxClient, "artifact.generated");

  assert.equal(artifactEventA.runId, "run-sse");
  assert.equal(artifactEventB.runId, "run-sse");
  assert.equal(artifactEventA.artifactHash, artifact.artifactHash);
  assert.equal(artifactEventB.artifactHash, artifact.artifactHash);

  await service.submitApprovalAction({
    runId: "run-sse",
    actor: "reviewer",
    action: "approve",
    planVersion: pausedArtifact.planVersion,
    graphVersion: pausedArtifact.graphVersion,
    artifactHash: pausedArtifact.artifactHash,
    nodeId: executableNode.id,
    nodeRevision: executableNode.revision,
    gateId: approvalGate.id,
  });

  const approvalEventA = await readUntilType(browserClient, "approval.accepted");
  const approvalEventB = await readUntilType(stxClient, "approval.accepted");

  assert.equal(approvalEventA.runId, "run-sse");
  assert.equal(approvalEventB.runId, "run-sse");
  assert.equal(approvalEventA.nodeId, executableNode.id);
  assert.equal(approvalEventB.nodeId, executableNode.id);
});

