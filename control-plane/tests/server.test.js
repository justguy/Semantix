class FakeSessionConnector {
  constructor() {
    this.notifications = new EventEmitter();
    this.threadCounter = 0;
    this.turnCounter = 0;
    this.threads = new Map();
  }

  onNotification(listener) {
    this.notifications.on("notification", listener);
    return () => {
      this.notifications.off("notification", listener);
    };
  }

  emit(method, params) {
    this.notifications.emit("notification", { method, params });
  }

  async healthCheck() {
    return {
      healthy: true,
      transport: "fake-app-server",
    };
  }

  async startThread(params = {}) {
    this.threadCounter += 1;
    const threadId = `runtime-thread-${this.threadCounter}`;
    const thread = {
      id: threadId,
      preview: "",
      status: { type: "idle" },
      cwd: params.cwd ?? "/workspace/project",
      turns: [],
    };
    this.threads.set(threadId, thread);
    queueMicrotask(() => {
      this.emit("thread/started", { thread });
    });

    return {
      thread,
      runtimeSessionId: threadId,
      model: "gpt-5.3-codex",
      modelProvider: "openai",
      cwd: thread.cwd,
      approvalPolicy: params.approvalPolicy ?? "never",
      sandbox: { type: "workspaceWrite" },
      reasoningEffort: null,
    };
  }

  async startTurn({ threadId, input }) {
    const thread = this.threads.get(threadId);
    this.turnCounter += 1;
    const turn = {
      id: `runtime-turn-${this.turnCounter}`,
      items: [],
      status: "inProgress",
      startedAt: Date.now(),
      completedAt: null,
      durationMs: null,
    };
    thread.status = { type: "active", activeFlags: [] };
    thread.preview = input.find((item) => item.type === "text")?.text ?? thread.preview;
    thread.turns.push({
      ...turn,
      items: input.map((item, index) => ({
        type: "userMessage",
        id: `item-${this.turnCounter}-${index + 1}`,
        content:
          item.type === "text"
            ? [{ type: "text", text: item.text, text_elements: [] }]
            : [],
      })),
    });

    queueMicrotask(() => {
      this.emit("thread/status/changed", {
        threadId,
        status: thread.status,
      });
      this.emit("turn/started", {
        threadId,
        turn,
      });
    });

    return {
      runtimeTurnId: turn.id,
      turn,
    };
  }

  completeTurn(threadId, turnId, status = "completed") {
    const thread = this.threads.get(threadId);
    const turn = thread?.turns.find((candidate) => candidate.id === turnId);
    if (!thread || !turn) {
      return;
    }

    turn.status = status;
    turn.completedAt = Date.now();
    turn.durationMs = Math.max(1, turn.completedAt - turn.startedAt);
    thread.status = { type: "idle" };
    this.emit("thread/status/changed", {
      threadId,
      status: thread.status,
    });
    this.emit("turn/completed", {
      threadId,
      turn,
    });
  }

  async readThread({ threadId }) {
    const thread = this.threads.get(threadId);
    return {
      ...thread,
      turns: [...thread.turns],
    };
  }

  async listTurns({ threadId }) {
    return [...(this.threads.get(threadId)?.turns ?? [])];
  }

  async interruptTurn({ threadId, turnId }) {
    this.completeTurn(threadId, turnId, "interrupted");
    return {};
  }
}

async function createHarness(t, options = {}) {
  const rootDir = await mkdtemp(join(tmpdir(), "semantix-control-plane-server-"));
  const workspaceRoot = join(rootDir, "workspace");
  await mkdir(workspaceRoot, { recursive: true });
  await mkdir(join(workspaceRoot, "src", "auth"), { recursive: true });
  await writeFile(
    join(workspaceRoot, "src", "auth", "verifyToken.js"),
    "export function verifyToken(token) {\n  return Boolean(token);\n}\n",
  );
  const store = new FileRunStore({
    rootDir,
  });
  const registry = new RuntimeRegistry();
  const sessionConnector = new FakeSessionConnector();
  const hostInvocations = [];
  const hostFunctionRegistry =
    options.hostFunctionRegistry ??
    new HostFunctionRegistry([
      {
        targetSymbol: DEFAULT_TARGET_SYMBOL,
        async preview(input) {
          return {
            id: "effect.preview.host",
            kind: "file",
            operation: "modify",
            target: input.workspace_path,
            summary: `Would modify ${input.workspace_path}.`,
            previewRef: "preview://host/1",
            diff: `diff --git a/${input.workspace_path} b/${input.workspace_path}\n@@ -1 +1 @@\n-old\n+new\n`,
            diffPreview: "@@ -1 +1 @@\n-old\n+new\n",
            policyState: "review_required",
            riskFlags: ["advisory_preview"],
            reversibility: {
              status: "reversible",
              mechanism: "local_vcs",
            },
            enforcement: {
              owner: "policy",
              status: "review_required",
              details: "Advisory only in v0.5.",
            },
          };
        },
        async invoke(input) {
          hostInvocations.push(input);
          return {
            outputSummary: `Applied ${input.summary}.`,
          };
        },
      },
    ]);
  registry.registerRuntimeAdapter(
    new CodexCliRuntimeAdapter({
      sessionConnector,
      runner:
        options.runner ??
        (async ({ runId, node }) => ({
          exitCode: 0,
          stdout: JSON.stringify(
            nodeRequiresCodeChangeProposal(node)
              ? createCodeChangeProposal({
                  workspaceRoot,
                  runId,
                  nodeId: node?.id,
                })
              : createSimpleSemanticProposal({
                  workspaceRoot,
                  runId,
                  nodeId: node?.id,
                }),
          ),
          stderr: "",
        })),
    }),
  );

  const service = new ControlPlaneService({
    store,
    runtimeRegistry: registry,
    hostFunctionRegistry,
  });

  t.after(async () => {
    await service.close();
    await rm(rootDir, { recursive: true, force: true });
  });

  return {
    service,
    sessionConnector,
    workspaceRoot,
    hostInvocations,
  };
}

